package spinMod;

public class TorqueVector {
    double i;
    double j;
    double k;

    public TorqueVector() {
        this.i = 0;
        this.j = 0;
        this.k = 0;
    }

    public TorqueVector(double i, double j, double k) {
        this.i = i;
        this.j = j;
        this.k = k;
    }
}
